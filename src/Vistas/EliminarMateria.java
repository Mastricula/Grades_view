    private void MostrarMateriasComboBox() {
        Connection conectado;
        Conexion conexion= new Conexion();
        conectado=conexion.Conectar();
        
        try{
            Statement statement=conectado.createStatement();
            ResultSet resultset=statement.executeQuery("SELECT materia FROM Materias");
            comboBoxMaterias.removeAllItems();
            while(resultset.next()){
                String materia = resultset.getString("materia");
                comboBoxMaterias.addItem(materia);
            }
        }
        catch(SQLException ex){
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);  
        }
    }
    
    private void Eliminar() {
        Administrador admin= new Administrador();
        String materiaNombre = (String)comboBoxMaterias.getSelectedItem();
        
        int respuesta =JOptionPane.showConfirmDialog(this, "¿Estás seguro de que quieres eliminar este Docente? '"+materiaNombre+"' ", "Seleccione su respuesta", JOptionPane.YES_NO_OPTION);
        if(respuesta==JOptionPane.YES_NO_OPTION)
        {
            admin.eliminarTablasMateria(materiaNombre);
            admin.EliminarMateria(materiaNombre);
            MostrarMateriasComboBox();     
        }
    }

