    private void comboBoxMateriascomboBoxDocentesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxMateriascomboBoxDocentesActionPerformed
        String materia = (String)comboBoxMaterias.getSelectedItem();

        if (materia == null) {
            return;
        }
        String materiaSelecionada = materia;

        Conexion conexion= new Conexion();
        Connection cx = conexion.Conectar();

        String nombreMateria;
        
        try{
            Statement statement= cx.createStatement();
            ResultSet rs = statement.executeQuery("SELECT * FROM Materias WHERE materia='"+materiaSelecionada+"'");
            while(rs.next()){
                nombreMateria =rs.getString("materia");
                
                inputNombre.setText(nombreMateria);
            }
        }
        catch(SQLException ex){
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_comboBoxMateriascomboBoxDocentesActionPerformed


